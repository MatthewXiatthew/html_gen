def eq(a, b):
    
    if a == b:
        return True
    return
    
def noteq(a, b):
    
    if a != b:
        return True
    return
    
def lt(a, b):
    
    if a < b:
        return True
    return
    
def gt(a, b):
    
    if a > b:
        return True
    return
    
def lteq(a, b):
    
    if a <= b:
        return True
    return

def gteq(a, b):
    
    if a >= b:
        return True